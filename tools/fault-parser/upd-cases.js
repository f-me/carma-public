var readline = require('readline')

var rd = readline.createInterface({
  input: process.stdin,
  output: process.stdout,
  terminal: false
})

rd.on('line', function(line) {
  var c = JSON.parse(line)
  var id = c.id.replace('case:', '')
  if (!id.match(/^\d+$/)) return

  for (k in c) {
    var v = tr[c[k]]
    if (v) c[k] = v
    else delete c[k]
  }

  Object.keys(c).length == 0 || console.log(
    'curl -X PUT -H "Content-Type: application/json"'
    + " -d '" + JSON.stringify(c) + "'"
    + " http://localhost:8000/_/case/" + id
  )
})
